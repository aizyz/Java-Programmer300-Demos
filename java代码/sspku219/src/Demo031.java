public class Demo031 {
    public static void main(String args[]) {
        String str = "seven";
        switch (str) {
            case "zero":
                System.out.println("零");
                break;
            case "one":
                System.out.println("壹");
                break;
            case "two":
                System.out.println("贰");
                break;
            case "three":
                System.out.println("叁");
                break;
            default:
                System.out.println("没有");
                break;
        }
    }

}



